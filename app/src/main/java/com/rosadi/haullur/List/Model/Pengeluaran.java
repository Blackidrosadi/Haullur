package com.rosadi.haullur.List.Model;

public class Pengeluaran {
    String id, idHaul, deskripsi, jumlahUang;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdHaul() {
        return idHaul;
    }

    public void setIdHaul(String idHaul) {
        this.idHaul = idHaul;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getJumlahUang() {
        return jumlahUang;
    }

    public void setJumlahUang(String jumlahUang) {
        this.jumlahUang = jumlahUang;
    }
}
