package com.rosadi.haullur.List.Model;

public class Artikel {

    private String id, fotoTamnel, judul, tanggal, lokasi, deskripsi, dilihat, idHaul;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFotoTamnel() {
        return fotoTamnel;
    }

    public void setFotoTamnel(String fotoTamnel) {
        this.fotoTamnel = fotoTamnel;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getLokasi() {
        return lokasi;
    }

    public void setLokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getDilihat() {
        return dilihat;
    }

    public void setDilihat(String dilihat) {
        this.dilihat = dilihat;
    }

    public String getIdHaul() {
        return idHaul;
    }

    public void setIdHaul(String idHaul) {
        this.idHaul = idHaul;
    }
}
