package com.rosadi.haullur.List.Model;

public class Penarikan {
    String id, idHaul, idKeluarga, jumlahUang, deskripsi, idAkun, nama, rt, jumlahAlmarhum;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdHaul() {
        return idHaul;
    }

    public void setIdHaul(String idHaul) {
        this.idHaul = idHaul;
    }

    public String getIdKeluarga() {
        return idKeluarga;
    }

    public void setIdKeluarga(String idKeluarga) {
        this.idKeluarga = idKeluarga;
    }

    public String getJumlahUang() {
        return jumlahUang;
    }

    public void setJumlahUang(String jumlahUang) {
        this.jumlahUang = jumlahUang;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getIdAkun() {
        return idAkun;
    }

    public void setIdAkun(String idAkun) {
        this.idAkun = idAkun;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getRt() {
        return rt;
    }

    public void setRt(String rt) {
        this.rt = rt;
    }

    public String getJumlahAlmarhum() {
        return jumlahAlmarhum;
    }

    public void setJumlahAlmarhum(String jumlahAlmarhum) {
        this.jumlahAlmarhum = jumlahAlmarhum;
    }
}
